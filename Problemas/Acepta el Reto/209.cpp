
#include <bits/stdc++.h>
using namespace std;

string line;
int solution[1000005];
vector<int> V;
void solve(){
    V.clear();
    //Assign phones
    V.push_back(-1);
    for(unsigned int i=0; i<line.length();i++) {
        if(line[i]=='T') V.push_back(i);
    }
    V.push_back(line.length());
    //End assign phones
    int cnt = 0;
    for(unsigned int i=0; i<line.length();i++) {
        if(line[i]=='T') solution[i]=3;
        else{
            // 1 == PENINSULA & 2 == ISLAS
            int leftPosition = V[cnt];
            int rigthPosition = V[cnt+1];
            while(rigthPosition<i) {
                cnt++;
                leftPosition = V[cnt];
                rigthPosition = V[cnt+1];
            }
            int distLeftPhone = abs(i-leftPosition); //primero es -1
            int distRightPhone = rigthPosition-i;
            int distSalidaLeft = i+1;
            int distSalidaRight = line.length()-i;
            //Si se está más cerca de una de las salidas que de un teléfono,
            //se preferirá correr hacia la salida.
            if(distSalidaLeft<=distRightPhone) solution[i]=1;
            else if(distSalidaRight<=distLeftPhone) solution[i]=2;
            //Si los teléfonos más próximos de ambos lados están a la misma
            //distancia, preferiremos ir hacia aquel que esté más cerca de una de las salidas del túnel.
            //Si en el escenario anterior vuelve a haber empate, se irá hacia la península.
            else if(leftPosition!=-1 && rigthPosition!=line.length() && distLeftPhone==distRightPhone && distSalidaLeft<=distSalidaRight) solution[i]=1;
            else if(leftPosition!=-1 && rigthPosition!=line.length() && distLeftPhone==distRightPhone && distSalidaLeft>distSalidaRight) solution[i]=2;
            else if(leftPosition!=-1 && rigthPosition!=line.length() && distLeftPhone<=distRightPhone) solution[i]=1;
            else if(leftPosition!=-1 && rigthPosition!=line.length() && distLeftPhone>distRightPhone) solution[i]=2;
            else if(leftPosition!=-1 && distLeftPhone<=distSalidaRight) solution[i]=1;
            else if(leftPosition==-1 && rigthPosition==line.length() && distSalidaLeft<=distSalidaRight) solution[i]=1;
            else solution[i]=2;
        }
    }
}

int main(){
    //freopen("C:/Users/Isaac/Documents/QT/Entregar/in.txt","r",stdin);
    //freopen("C:/Users/Isaac/Documents/QT/Entregar/out.txt","w",stdout);
    while(getline(cin,line)){
        solve();
        int cases,num; scanf("%d",&cases);
        for(int i=0; i<cases;i++){
            scanf("%d",&num); num--;
            if(solution[num]==1) printf("PENINSULA\n");
            else if(solution[num]==2) printf("ISLAS\n");
            else if(solution[num]==3) printf("AQUI\n");
        }
        cin.ignore();
    }
    return 0;
}

